use std::collections::BTreeMap;
use std::env;
use std::fs;
use std::path::{Path, PathBuf};
use std::process::Command;

use anyhow::{Context, Result, anyhow, bail};
use serde::Deserialize;

const COMMANDS_BEGIN: &str = "<!-- BEGIN:OWUI_LINT_COMMANDS -->";
const COMMANDS_END: &str = "<!-- END:OWUI_LINT_COMMANDS -->";
const RULES_BEGIN: &str = "<!-- BEGIN:OWUI_LINT_RULES -->";
const RULES_END: &str = "<!-- END:OWUI_LINT_RULES -->";

const SECTION_BREAKS: &[&str] = &["Arguments:", "Options:", "Commands:", "Examples:"];

#[derive(Debug)]
struct CommandDoc {
    name: String,
    description: String,
    usage: String,
}

#[derive(Debug)]
struct OptionDoc {
    option: String,
    description: String,
}

#[derive(Debug, Deserialize)]
struct RulesPayload {
    rules: Vec<RuleJson>,
}

#[derive(Debug, Deserialize)]
struct RuleJson {
    id: String,
    default_severity: String,
    title: String,
    summary: String,
}

fn main() -> Result<()> {
    let args: Vec<String> = env::args().skip(1).collect();
    let mut check = false;
    let mut write = false;

    for arg in args {
        match arg.as_str() {
            "--check" => check = true,
            "--write" => write = true,
            "-h" | "--help" => {
                println!("Usage: cargo run --bin docs-sync -- [--check|--write]");
                println!("  --check  Fail if README.md is out of date");
                println!("  --write  Rewrite README.md generated sections (default)");
                return Ok(());
            }
            other => bail!("Unknown argument: {other}"),
        }
    }

    let root = PathBuf::from(env!("CARGO_MANIFEST_DIR"));
    let readme_path = root.join("README.md");
    let original = fs::read_to_string(&readme_path)
        .with_context(|| format!("failed to read {}", readme_path.display()))?;
    let generated = build_readme_content(&original)?;

    let check_mode = check && !write;
    if check_mode {
        if generated != original {
            eprintln!("README.md is out of date. Run: make docs-sync");
            std::process::exit(1);
        }
        println!("README.md is up to date.");
        return Ok(());
    }

    if generated != original {
        let final_text = if generated.ends_with('\n') {
            generated
        } else {
            format!("{generated}\n")
        };
        fs::write(&readme_path, final_text)
            .with_context(|| format!("failed to write {}", readme_path.display()))?;
        println!("Updated README.md");
    } else {
        println!("README.md already up to date");
    }

    Ok(())
}

fn build_readme_content(original: &str) -> Result<String> {
    let commands = render_commands_block()?;
    let rules = render_rules_block()?;
    let updated = replace_marker_block(original, COMMANDS_BEGIN, COMMANDS_END, &commands)?;
    replace_marker_block(&updated, RULES_BEGIN, RULES_END, &rules)
}

fn render_commands_block() -> Result<String> {
    let top_help = run_owui_lint(&["--help"])?;
    let subcommands = parse_commands(&top_help);
    let mut command_docs = vec![CommandDoc {
        name: "owui-lint".to_string(),
        description: "Top-level command".to_string(),
        usage: parse_usage(&top_help),
    }];

    for cmd in subcommands {
        let sub_help = run_owui_lint(&[&cmd.name, "--help"])?;
        command_docs.push(CommandDoc {
            name: format!("owui-lint {}", cmd.name),
            description: cmd.description,
            usage: parse_usage(&sub_help),
        });
    }

    let global_options: Vec<OptionDoc> = parse_options(&top_help)
        .into_iter()
        .map(normalize_option)
        .collect();

    let mut lines = vec![
        "_This section is generated by `src/bin/docs-sync.rs`. Do not edit manually._".to_string(),
        String::new(),
        "### Commands".to_string(),
        String::new(),
        "| Command | Description | Usage |".to_string(),
        "|------|-------------|-------|".to_string(),
    ];

    for cmd in &command_docs {
        lines.push(format!(
            "| `{}` | {} | `{}` |",
            escape_cell(&cmd.name),
            escape_cell(&cmd.description),
            escape_cell(&cmd.usage)
        ));
    }

    lines.extend([
        String::new(),
        "### Global Options".to_string(),
        String::new(),
        "| Option | Description |".to_string(),
        "|--------|-------------|".to_string(),
    ]);

    for opt in &global_options {
        lines.push(format!(
            "| `{}` | {} |",
            escape_cell(&opt.option),
            escape_cell(&opt.description)
        ));
    }

    lines.extend([
        String::new(),
        "### Quick Examples".to_string(),
        String::new(),
        "```bash".to_string(),
        "owui-lint path/to/extensions".to_string(),
        "owui-lint path/to/extensions --format sarif --output owui-lint.sarif".to_string(),
        "owui-lint rules".to_string(),
        "owui-lint explain OWT101".to_string(),
        "```".to_string(),
    ]);

    Ok(lines.join("\n"))
}

fn render_rules_block() -> Result<String> {
    let payload: RulesPayload =
        serde_json::from_str(&run_owui_lint(&["rules", "--format", "json"])?)
            .context("failed to parse `owui-lint rules --format json` output")?;

    let mut grouped: BTreeMap<&str, Vec<RuleJson>> = BTreeMap::new();
    grouped.insert("OWUI", Vec::new());
    grouped.insert("OWT", Vec::new());
    grouped.insert("OWP", Vec::new());
    grouped.insert("OWF", Vec::new());
    grouped.insert("OWA", Vec::new());
    grouped.insert("OWPL", Vec::new());
    grouped.insert("OTHER", Vec::new());

    for rule in payload.rules {
        let group = group_for_rule(&rule.id);
        grouped
            .get_mut(group)
            .expect("known group key should exist")
            .push(rule);
    }

    let mut lines = vec![
        "_This section is generated by `src/bin/docs-sync.rs` from `owui-lint rules --format json`. Do not edit manually._".to_string(),
        String::new(),
    ];

    let order = ["OWUI", "OWT", "OWP", "OWF", "OWA", "OWPL", "OTHER"];
    for group in order {
        let rows = grouped
            .get(group)
            .expect("known group key should exist for rendering");
        if rows.is_empty() {
            continue;
        }

        lines.push(format!("### {}", group_label(group)));
        lines.push(String::new());
        lines.push("| Rule | Severity | Title | What it checks |".to_string());
        lines.push("|------|----------|-------|----------------|".to_string());
        for rule in rows {
            lines.push(format!(
                "| `{}` | {} | {} | {} |",
                escape_cell(&rule.id),
                escape_cell(&rule.default_severity),
                escape_cell(&rule.title),
                escape_cell(&rule.summary)
            ));
        }
        lines.push(String::new());
    }

    Ok(lines.join("\n").trim_end().to_string())
}

fn group_label(group: &str) -> &'static str {
    match group {
        "OWUI" => "Universal (all extension types)",
        "OWT" => "Tools (`OWT`)",
        "OWP" => "Pipe (`OWP`)",
        "OWF" => "Filter (`OWF`)",
        "OWA" => "Action (`OWA`)",
        "OWPL" => "Pipeline (`OWPL`)",
        _ => "Other",
    }
}

fn group_for_rule(rule_id: &str) -> &str {
    if rule_id.starts_with("OWPL") {
        return "OWPL";
    }
    if rule_id.starts_with("OWUI") {
        return "OWUI";
    }
    if rule_id.starts_with("OWT") {
        return "OWT";
    }
    if rule_id.starts_with("OWP") {
        return "OWP";
    }
    if rule_id.starts_with("OWF") {
        return "OWF";
    }
    if rule_id.starts_with("OWA") {
        return "OWA";
    }
    "OTHER"
}

fn run_owui_lint(args: &[&str]) -> Result<String> {
    if env::var("OWUI_LINT_DOCS_USE_DOCKER").as_deref() == Ok("1") {
        let target = args.join(" ");
        let output = Command::new("make")
            .arg("docker-run")
            .arg(format!("TARGET={target}"))
            .output()
            .context("failed to execute `make docker-run`")?;
        return handle_command_output(output, true);
    }

    if let Some(bin_path) = nearby_owui_lint_binary() {
        let output = Command::new(bin_path)
            .args(args)
            .output()
            .context("failed to execute nearby owui-lint binary")?;
        return handle_command_output(output, false);
    }

    let output = Command::new("cargo")
        .arg("run")
        .arg("--locked")
        .arg("--bin")
        .arg("owui-lint")
        .arg("--")
        .args(args)
        .output()
        .context("failed to execute `cargo run --bin owui-lint -- ...`")?;
    handle_command_output(output, false)
}

fn nearby_owui_lint_binary() -> Option<PathBuf> {
    let current = env::current_exe().ok()?;
    let mut candidate = current.parent()?.join("owui-lint");
    if cfg!(windows) {
        candidate.set_extension("exe");
    }
    if candidate.exists() {
        Some(candidate)
    } else {
        None
    }
}

fn handle_command_output(output: std::process::Output, docker_prefixed: bool) -> Result<String> {
    if !output.status.success() {
        let stderr = String::from_utf8_lossy(&output.stderr).trim().to_string();
        let stdout = String::from_utf8_lossy(&output.stdout).trim().to_string();
        let detail = if !stderr.is_empty() {
            stderr
        } else if !stdout.is_empty() {
            stdout
        } else {
            "(no output)".to_string()
        };
        bail!("owui-lint command failed: {detail}");
    }

    let mut stdout =
        String::from_utf8(output.stdout).context("command output was not valid UTF-8")?;
    if docker_prefixed {
        let mut iter = stdout.lines();
        if let Some(first) = iter.next()
            && first.starts_with("docker run ")
        {
            stdout = iter.collect::<Vec<_>>().join("\n");
        }
    }

    Ok(stdout.trim().to_string())
}

fn parse_commands(help_text: &str) -> Vec<CommandDoc> {
    let mut out = Vec::new();
    for line in section_lines(help_text, "Commands:") {
        let stripped = line.trim();
        if stripped.is_empty() {
            continue;
        }
        let mut parts = stripped.splitn(2, char::is_whitespace);
        let Some(name) = parts.next() else {
            continue;
        };
        if name == "help" {
            continue;
        }
        let description = parts.next().map(str::trim).unwrap_or_default().to_string();

        out.push(CommandDoc {
            name: name.to_string(),
            description,
            usage: String::new(),
        });
    }
    out
}

fn parse_usage(help_text: &str) -> String {
    for line in help_text.lines() {
        let trimmed = line.trim();
        if let Some(rest) = trimmed.strip_prefix("Usage:") {
            return rest.trim().to_string();
        }
    }
    String::new()
}

fn parse_options(help_text: &str) -> Vec<OptionDoc> {
    let mut options = Vec::new();
    let mut current_opt: Option<String> = None;
    let mut current_desc: Vec<String> = Vec::new();

    let flush = |options: &mut Vec<OptionDoc>,
                 current_opt: &mut Option<String>,
                 current_desc: &mut Vec<String>| {
        if let Some(opt) = current_opt.take() {
            let desc = current_desc
                .iter()
                .map(|s| s.trim())
                .filter(|s| !s.is_empty())
                .collect::<Vec<_>>()
                .join(" ");
            options.push(OptionDoc {
                option: opt,
                description: desc,
            });
            current_desc.clear();
        }
    };

    for raw in section_lines(help_text, "Options:") {
        let stripped = raw.trim();
        if stripped.is_empty() {
            continue;
        }

        if stripped.starts_with('-') {
            flush(&mut options, &mut current_opt, &mut current_desc);
            if let Some((opt, desc)) = split_double_space(stripped) {
                current_opt = Some(opt.to_string());
                current_desc.push(desc.to_string());
            } else {
                current_opt = Some(stripped.to_string());
            }
        } else if current_opt.is_some() {
            current_desc.push(stripped.to_string());
        }
    }

    flush(&mut options, &mut current_opt, &mut current_desc);
    options
}

fn split_double_space(input: &str) -> Option<(&str, &str)> {
    let bytes = input.as_bytes();
    for i in 0..bytes.len().saturating_sub(1) {
        if bytes[i] == b' ' && bytes[i + 1] == b' ' {
            let left = input[..i].trim_end();
            let right = input[i..].trim_start();
            if !left.is_empty() && !right.is_empty() {
                return Some((left, right));
            }
        }
    }
    None
}

fn normalize_option(option: OptionDoc) -> OptionDoc {
    let mut desc = option.description.replace("[default:", "Default:");
    desc = desc.replace("[possible values:", "Possible values:");
    if desc.contains("Default:") || desc.contains("Possible values:") {
        desc = desc.replace(']', "");
    }
    desc = desc.split_whitespace().collect::<Vec<_>>().join(" ");

    OptionDoc {
        option: option.option,
        description: desc,
    }
}

fn section_lines(help_text: &str, header: &str) -> Vec<String> {
    let lines: Vec<&str> = help_text.lines().collect();
    let Some(mut idx) = lines.iter().position(|line| line.trim() == header) else {
        return Vec::new();
    };
    idx += 1;

    let mut out = Vec::new();
    while idx < lines.len() {
        let line = lines[idx];
        if SECTION_BREAKS.iter().any(|marker| line.trim() == *marker) {
            break;
        }
        out.push(line.to_string());
        idx += 1;
    }
    out
}

fn replace_marker_block(content: &str, begin: &str, end: &str, generated: &str) -> Result<String> {
    let Some(start) = content.find(begin) else {
        return Err(anyhow!("missing marker block: {begin} ... {end}"));
    };
    let Some(end_rel) = content[start..].find(end) else {
        return Err(anyhow!("missing marker block end: {end}"));
    };
    let end_idx = start + end_rel + end.len();

    let mut out = String::new();
    out.push_str(&content[..start]);
    out.push_str(begin);
    out.push('\n');
    out.push_str(generated);
    out.push('\n');
    out.push_str(end);
    out.push_str(&content[end_idx..]);
    Ok(out)
}

fn escape_cell(text: &str) -> String {
    text.replace('|', "\\|")
}

#[allow(dead_code)]
fn _is_repo_root(path: &Path) -> bool {
    path.join("Cargo.toml").exists() && path.join("README.md").exists()
}
